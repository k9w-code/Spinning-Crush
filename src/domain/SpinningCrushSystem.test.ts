function testロジックB_バトル更新() {
  console.log('--- テスト: ロジックB (バトル物理・移行トリガー) ---');

  const プレイヤーギア = アセンブル実行('c001', 'b101_f', 'w101_f', 's101_f', 1, パーツマスタ, チップマスタ, 奥義マスタ);
  const エネミーギア = アセンブル実行('c001', 'b101_f', 'w101_f', 's101_f', 1, パーツマスタ, チップマスタ, 奥義マスタ);

  const マネージャー = new バトル更新マネージャー(プレイヤーギア, エネミーギア);

  // モビリティの速度計算テスト
  const 速度 = マネージャー.get移動速度(100);
  assert(速度 === 2.5 + 100 * 0.015, `モビリティ100の速度は ${2.5 + 100 * 0.015} である (実際: ${速度})`);

  // スピンロステスト (60フレームで3減少)
  const 初期ライフ = マネージャー.プレイヤーライフ;
  for (let i = 0; i < 60; i++) {
    // 衝突を防ぐために位置と速度を固定
    マネージャー.プレイヤー位置X = 200;
    マネージャー.プレイヤー位置Y = 300;
    マネージャー.プレイヤー速度X = 0;
    マネージャー.プレイヤー速度Y = 0;
    
    マネージャー.エネミー位置X = 600;
    マネージャー.エネミー位置Y = 300;
    マネージャー.エネミー速度X = 0;
    マネージャー.エネミー速度Y = 0;
    
    マネージャー.update(null);
  }
  const 減少後ライフ = マネージャー.プレイヤーライフ;
  assert(初期ライフ - 減少後ライフ === 2, `1秒経過でライフが2減少している (初期: ${初期ライフ}, 減少後: ${減少後ライフ})`);

  // フェーズ移行条件テスト
  // 1. ゲージ100未満 ＆ 間合い内 ＆ Fキー => 移行しない
  マネージャー.プレイヤー攻撃ゲージ = 50;
  // プレイヤーと敵の位置を近づける (間合い50ピクセル)
  マネージャー.プレイヤー位置X = 100;
  マネージャー.エネミー位置X = 150;
  マネージャー.update('F');
  assert(マネージャー.現在フェーズ === 'ディスタンス', 'ゲージ不足時はコマンドフェーズに移行しない');

  // 2. ゲージ100 ＆ レンジ外 ＆ Fキー => 移行しない
  マネージャー.プレイヤー攻撃ゲージ = 100;
  // プレイヤーと敵の位置を遠ざける (間合い500ピクセル)
  マネージャー.プレイヤー位置X = 100;
  マネージャー.エネミー位置X = 600;
  マネージャー.update('F');
  assert(マネージャー.現在フェーズ === 'ディスタンス', 'レンジ外の時はコマンドフェーズに移行しない');

  // 3. ゲージ100 ＆ レンジ内 ＆ Fキー => コマンドフェーズ移行
  マネージャー.プレイヤー攻撃ゲージ = 100;
  マネージャー.プレイヤー位置X = 100;
  マネージャー.エネミー位置X = 120; // 間合い 20ピクセル (初期装備レンジはレンジ100以上ある)
  マネージャー.update('F');
  assert(マネージャー.現在フェーズ === 'コマンド', 'ゲージ100かつレンジ内のFキーでコマンドフェーズに移行する');
  assert(マネージャー.攻撃側 === 'プレイヤー', '仕掛けたプレイヤーが攻撃側になる');

  // 確率計算テスト
  const 回避率 = マネージャー.get最終回避率(100, 100, 10);
  assert(回避率 === 30 + (100 - 100)/20 + 10, `回避率計算が数式通りである (実際: ${回避率})`);

  const カウンター率 = マネージャー.get最終カウンター成功率(100, 100, -10);
  assert(カウンター率 === 25 + (100 - 100)/40 - 10, `カウンター率計算が数式通りである (実際: ${カウンター率})`);

  // 反動クラッシュ
  マネージャー.実行反動クラッシュ();
  assert(マネージャー.現在フェーズ === 'ディスタンス', '反動クラッシュ後はディスタンスフェーズに戻る');
  assert(Math.floor(マネージャー.プレイヤー攻撃ゲージ) === 100, '攻撃ゲージは反動クラッシュ単体では減算されず残量を維持する');
  assert(マネージャー.プレイヤー速度X < 0 && マネージャー.エネミー速度X > 0, '反動クラッシュによって互いに外向きの速度ベクトルが発生する');
}

function testロジックC_報酬ドロップ() {
  console.log('--- テスト: ロジックC (リザルト報酬) ---');

  let ドロップカウンタ = 0;
  let 所持JP = 0;
  let インベントリ: string[] = [];

  // 1. 未所持パーツのドロップ
  const 結果1 = 報酬ドロップ処理(ドロップカウンタ, 所持JP, インベントリ, パーツマスタ, セリフマスタ);
  console.log('獲得したパーツID:', 結果1.獲得パーツID, '案内テキスト:', 結果1.案内テキスト);
  assert(結果1.新規獲得フラグ === true, '新規獲得フラグが真である');
  assert(結果1.更新インベントリ.includes(結果1.獲得パーツID), '獲得したパーツがインベントリに追加されている');
  assert(結果1.更新ドロップカウンタ === 1, 'カウンタが1にインクリメントされている');
  assert(結果1.更新所持JP === 0, 'JPは増えていない');

  // 2. 既所持パーツのドロップ（リサイクル）
  所持JP = 結果1.更新所持JP;
  インベントリ = 結果1.更新インベントリ;
  ドロップカウンタ = 0; // ブレードを再度引くためにカウンタを0に戻す

  // インベントリにランク1のブレードを全て登録して、強制的に重複させる
  const ランク1ブレード = パーツマスタ.filter(p => p.種別 === "1" && p.ランク === "1").map(p => p.パーツID);
  インベントリ = [...ランク1ブレード];

  const 結果2 = 報酬ドロップ処理(ドロップカウンタ, 所持JP, インベントリ, パーツマスタ, セリフマスタ);
  console.log('重複獲得案内テキスト:', 結果2.案内テキスト);
  assert(結果2.新規獲得フラグ === false, '重複したため新規獲得フラグは偽である');
  assert(結果2.更新所持JP === 所持JP + 10, `JPが10加算されている (前: ${所持JP}, 後: ${結果2.更新所持JP})`);
  assert(結果2.案内テキスト.includes('リサイクル') || 結果2.案内テキスト.includes('10JP'), 'リサイクルの案内テキストが出力されている');
}

function testステージ進行() {
  console.log('--- テスト: ステージ進行マネージャー ---');

  const 進行マネージャー = new ステージ進行マネージャー(エネミーマスタ);

  const 敵1 = 進行マネージャー.get現在の対戦相手();
  assert(敵1 !== null && 敵1.エネミー名 === 'コハル', `1戦目の敵は「コハル」である (実際: ${敵1?.エネミー名})`);

  進行マネージャー.勝利時進行();
  const 敵2 = 進行マネージャー.get現在の対戦相手();
  assert(敵2 !== null && 敵2.エネミー名 === 'レツ', `2戦目の敵は「レツ」である (実際: ${敵2?.エネミー名})`);

  // ボスまで進める
  進行マネージャー.勝利時進行(); // ガイ
  進行マネージャー.勝利時進行(); // ハル
  進行マネージャー.勝利時進行(); // ゴウ
  
  const ボス = 進行マネージャー.get現在の対戦相手();
  assert(ボス !== null && ボス.エネミー名 === 'ゴウ', `5戦目の敵は「ゴウ」である (実際: ${ボス?.エネミー名})`);
  assert(ボス?.ボスフラグ === '1', 'ゴウのボスフラグは1である');

  進行マネージャー.勝利時進行();
  assert(進行マネージャー.isステージクリア() === true, '5人全員撃破でステージクリアとなる');
}

