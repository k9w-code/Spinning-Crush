const ATTRIBUTES = [
  { code: 'f', name: '火' },
  { code: 'a', name: '風' },
  { code: 'e', name: '土' },
  { code: 'w', name: '水' },
  { code: 'n', name: '無' }
];

const PARTS_TYPES = [
  { code: '1', key: 'b', names: BLADE_NAMES },
  { code: '2', key: 'w', names: WEIGHT_NAMES },
  { code: '3', key: 's', names: SOLE_NAMES }
];

// CSV出力生成
let csvContent = '"パーツID","パーツ名","種別","画像ID","ランク","属性","ライフ","アタック","ディフェンス","スピード","レンジ","モビリティ","フレーバー","ステ確認","メモ"\n';

// ランク1からランク6までのすべての組み合わせを出力
for (let rank = 1; rank <= 6; rank++) {
  for (const type of PARTS_TYPES) {
    for (let kindNum = 1; kindNum <= 8; kindNum++) {
      const kindStr = kindNum.toString().padStart(2, '0');
      const baseName = type.names[kindStr];
      const decoratedName = RANK_DECORATIONS[rank](baseName);

      for (const attr of ATTRIBUTES) {
        const partId = `${type.key}${rank}${kindStr}_${attr.code}`;
        // 画像は以前の2D Canvas用に、部位・種類・属性のみ引き継ぐ（例: b001_f）
        const imageId = `${type.key}0${kindStr}_${attr.code}`;

        // 基準ステータスを取得
        const base = BASE_STATS[type.code][kindStr];

        // ランク倍率
        const stats = {
          ライフ: base.ライフ * rank,
          アタック: base.アタック * rank,
          ディフェンス: base.ディフェンス * rank,
          スピード: base.スピード * rank,
          レンジ: base.レンジ * rank,
          モビリティ: base.モビリティ * rank
        };

        const total = Object.values(stats).reduce((a, b) => a + b, 0);

        csvContent += `"${partId}","${decoratedName}","${type.code}","${imageId}","${rank}","${attr.name}","${stats.ライフ}","${stats.アタック}","${stats.ディフェンス}","${stats.スピード}","${stats.レンジ}","${stats.モビリティ}","工業規格パーツ","${total}","ランク${rank}"\n`;
      }
    }
  }
}

// 保存
const destPath = path.join(__dirname, '../public/sheets/パーツマスタ.csv');
fs.writeFileSync(destPath, csvContent, 'utf8');
console.log('Successfully generated parts master CSV!');

// タブ区切り（TSV）バージョンも生成 (スプレッドシートへのダイレクトコピペ用)
const tsvContent = csvContent
  .split('\n')
  .map(line => {
    // ダブルクォートを除去し、カンマをタブ文字（\t）に置換
    return line.replace(/"/g, '').replace(/,/g, '\t');
  })
  .join('\n');

const tsvDestPath = path.join(__dirname, '../public/sheets/パーツマスタ_コピペ用.txt');
fs.writeFileSync(tsvDestPath, tsvContent, 'utf8');
console.log('Successfully generated parts master TSV for copy-paste!');
